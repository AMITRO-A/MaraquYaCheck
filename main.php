<script>
    document.addEventListener('DOMContentLoaded', function() {
        const carousel = document.querySelector('.carousel');
        const images = document.querySelectorAll('.carousel-image');
        const prevBtn = document.querySelector('.carousel-control.prev');
        const nextBtn = document.querySelector('.carousel-control.next');
        const imageWidth = images[0].clientWidth + 16; // Ширина изображения с учетом отступов
        let currentPosition = 0;

        function updateCarousel(direction) {
            const visibleImages = 3;
            const maxPosition = -(imageWidth * (images.length - visibleImages));

            if (direction === 'prev') {
                currentPosition = Math.min(currentPosition + imageWidth, 0);
            } else {
                currentPosition = Math.max(currentPosition - imageWidth, maxPosition);
            }

            carousel.style.transform = `translateX(${currentPosition}px)`;
        }

        prevBtn.addEventListener('click', () => updateCarousel('prev'));
        nextBtn.addEventListener('click', () => updateCarousel('next'));
    });
</script>
